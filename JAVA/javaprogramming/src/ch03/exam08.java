public class exam08
{

	public static void main(String[] args)
	{
		int a = 10;
		int b = 20;
		int c = 30;
		
		System.out.println(a<b); // 10 < 20 true
		System.out.println(a>b); // 10 > 20 false
		System.out.println(a+b<=c); // 10+20 <= 30 true
		System.out.println(a+b>=c); // 10+20 >= 30 true
	}

}